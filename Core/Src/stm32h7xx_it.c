/**
  * @brief This function handles SPI2 global interrupt.
  */
void SPI2_IRQHandler(void)
{
  /* USER CODE BEGIN SPI2_IRQn 0 */
    if(LL_SPI_IsActiveFlag_OVR(SPI2) || LL_SPI_IsActiveFlag_UDR(SPI2)
       || LL_SPI_IsActiveFlag_CRCERR(SPI2))
    {
      /* Call Error function */
      SPI_TransferError_Callback(SPI2);
    }
    /* Check RXP flag value in ISR register */
    if(LL_SPI_IsActiveFlag_EOT(SPI2) && LL_SPI_IsEnabledIT_EOT(SPI2))
    {
      /* EOT flag could be used too as transfer size is equal to 1 */
      /* Call function Reception Callback */
      SPI_Rx_Callback(SPI2);
    }
  /* USER CODE END SPI2_IRQn 0 */
  /* USER CODE BEGIN SPI2_IRQn 1 */

  /* USER CODE END SPI2_IRQn 1 */
}

